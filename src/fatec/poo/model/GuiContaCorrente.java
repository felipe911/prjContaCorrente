    private void btnSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacarActionPerformed
        
        if(Double.parseDouble(txtValor.getText()) > objContaCorrente.getSaldo()){
            JOptionPane.showMessageDialog(null, "Saldo Insuficiente", "AVISO", JOptionPane.ERROR_MESSAGE);
        }
        else{
            objContaCorrente.sacar(Double.parseDouble(txtValor.getText()));
            txtSaldo.setText(String.valueOf(objContaCorrente.getSaldo()));
        }

    }//GEN-LAST:event_btnSacarActionPerformed

    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        objContaCorrente.depositar(Double.parseDouble(txtValor.getText()));
        txtSaldo.setText(String.valueOf(objContaCorrente.getSaldo()));
    }//GEN-LAST:event_btnDepositarActionPerformed

